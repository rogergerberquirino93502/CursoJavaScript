//Asignacion

var a = 3;
var b = 5;

console.log(a);

//Asignacion de suma
a += b;
console.log(a);

//Asignacion de resta
a -= b;
console.log(a);

//Asignacion de multiplicacion
a *= b;
console.log(a);

//Asignacion de division
a /= b;
console.log(a);

//Asignacion de modulo
a %= b;
console.log(a);

//Asignacion de exponenciacion
a **= b;
console.log(a);

